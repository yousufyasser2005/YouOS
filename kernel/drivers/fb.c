#include <kernel/fb.h>

/* ── Framebuffer state ───────────────────────────────────────── */
static fb_info_t fb;
static int fb_ready = 0;

void fb_init(uint64_t addr, uint32_t width, uint32_t height,
             uint32_t pitch, uint8_t bpp)
{
    fb.addr   = addr;
    fb.width  = width;
    fb.height = height;
    fb.pitch  = pitch;
    fb.bpp    = bpp;
    fb_ready  = 1;
}

int fb_available(void) { return fb_ready; }

/* ── Pixel operations ────────────────────────────────────────── */
void fb_put_pixel(int x, int y, uint32_t color)
{
    if (!fb_ready || x < 0 || y < 0 ||
        (uint32_t)x >= fb.width || (uint32_t)y >= fb.height) return;
    uint32_t* p = (uint32_t*)(fb.addr + (uint32_t)y * fb.pitch
                              + (uint32_t)x * (fb.bpp / 8));
    *p = color;
}

void fb_fill(uint32_t color)
{
    if (!fb_ready) return;
    for (uint32_t y = 0; y < fb.height; y++)
        for (uint32_t x = 0; x < fb.width; x++)
            fb_put_pixel((int)x, (int)y, color);
}

void fb_draw_rect(int x, int y, int w, int h, uint32_t color)
{
    for (int row = y; row < y + h; row++)
        for (int col = x; col < x + w; col++)
            fb_put_pixel(col, row, color);
}

