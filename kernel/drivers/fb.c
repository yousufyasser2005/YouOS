void fb_draw_char(int x, int y, char c, uint32_t fg, uint32_t bg)
{
    if (!fb_ready) return;
    int idx = (unsigned char)c - 32;
    if (idx < 0 || idx >= 96) idx = 0;
    const uint8_t* glyph = font8x16[idx];
    for (int row = 0; row < 16; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            fb_put_pixel(x + col, y + row, color);
        }
    }
}

void fb_draw_string(int x, int y, const char* s, uint32_t fg, uint32_t bg)
{
    while (*s) {
        fb_draw_char(x, y, *s++, fg, bg);
        x += 8;
    }
}

/* ── Software terminal ───────────────────────────────────────── */
#define TERM_CHAR_W  8
#define TERM_CHAR_H  16
#define TERM_COLS    (1024 / TERM_CHAR_W)   /* 128 */
#define TERM_ROWS    (768  / TERM_CHAR_H)   /* 48  */

static int     term_col  = 0;
static int     term_row  = 0;
static uint32_t term_fg  = FB_WHITE;
static uint32_t term_bg  = FB_BLACK;

static void term_scroll(void)
{
    /* Scroll up by one row: copy pixels up */
    uint32_t row_bytes = (uint32_t)TERM_CHAR_H * fb.pitch;
    uint8_t* dst = (uint8_t*)fb.addr;
    uint8_t* src = dst + row_bytes;
    uint32_t copy_bytes = ((uint32_t)TERM_ROWS - 1) * row_bytes;
    for (uint32_t i = 0; i < copy_bytes; i++) dst[i] = src[i];
    /* Clear last row */
    fb_draw_rect(0, (TERM_ROWS - 1) * TERM_CHAR_H,
                 TERM_COLS * TERM_CHAR_W, TERM_CHAR_H, term_bg);
}

void fb_terminal_init(void)
{
    if (!fb_ready) return;
    fb_fill(FB_BLACK);
    term_col = 0;
    term_row = 0;
    term_fg  = FB_WHITE;
    term_bg  = FB_BLACK;
}

void fb_terminal_set_color(uint32_t fg, uint32_t bg)
{
    term_fg = fg;
    term_bg = bg;
}

void fb_terminal_putchar(char c)
{
    if (!fb_ready) return;
    if (c == '\n') {
        term_col = 0;
        term_row++;
        if (term_row >= TERM_ROWS) { term_scroll(); term_row = TERM_ROWS - 1; }
        return;
    }
    if (c == '\r') { term_col = 0; return; }
    if (c == '\b') {
        if (term_col > 0) {
            term_col--;
            fb_draw_char(term_col * TERM_CHAR_W, term_row * TERM_CHAR_H,
                         ' ', term_fg, term_bg);
        }
        return;
    }
    fb_draw_char(term_col * TERM_CHAR_W, term_row * TERM_CHAR_H,
                 c, term_fg, term_bg);
    term_col++;
    if (term_col >= TERM_COLS) {
        term_col = 0;
        term_row++;
        if (term_row >= TERM_ROWS) { term_scroll(); term_row = TERM_ROWS - 1; }
    }
}

void fb_terminal_puts(const char* s)
{
    while (*s) fb_terminal_putchar(*s++);
}

void fb_terminal_puts_color(const char* s, uint32_t fg, uint32_t bg)
{
    uint32_t old_fg = term_fg, old_bg = term_bg;
    term_fg = fg; term_bg = bg;
    while (*s) fb_terminal_putchar(*s++);
    term_fg = old_fg; term_bg = old_bg;
}
